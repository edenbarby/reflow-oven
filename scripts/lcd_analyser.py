buf = []
with open('gdb-frame-output.txt', 'r') as file:
    for line in file:
        buf.append(int(re.sub('[^0-9a-fA-FxX]', '', line), 16))

col_max = 84
row_max = 48

screen = ['' for _ in range(row_max)]
for i, data in enumerate(buf):
    for j in range(8):
        y = 8*math.floor(i / col_max) + j
        if data & (1 << j):
            screen[y] += '+'
        else:
            screen[y] += ' '

top = ' '
for x in range(84):
    top += str(x%8)
print(top)
for y, line in enumerate(screen):
    print('{} {}'.format(y%8, line))
